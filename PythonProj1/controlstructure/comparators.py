"""
== --> Value Equality
!= --> Not equal to
< --> Less than
> --> Greater than
<= --> Less than or equal to
>= --> Greater than or equal to
"""

bool_one = 10 == 11
print(bool_one)
#print("The boolean representation of 'bool_one = 10 == 11' is " + (bool_one))
not_equal = 10 != 11
print(not_equal)
less_than = 10 < 11
print(less_than)
greater_than = 10 > 9
print(greater_than)
lt_eq = 10 <= 10
print(lt_eq)
gt_eq = 10 >= 11 - 1
print (gt_eq)
